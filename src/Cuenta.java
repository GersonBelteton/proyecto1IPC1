
public class Cuenta {
    
    private int numero;
    private double dinero;
    
    public Cuenta(int numero, double dinero){
        this.numero = numero;
        this.dinero = dinero;
    }

    public int getNumero() {
        return numero;
    }

    public void setNumero(int numero) {
        this.numero = numero;
    }

    public double getDinero() {
        return dinero;
    }

    public void setDinero(double dinero) {
        this.dinero = dinero;
    }
    
    
    
}
